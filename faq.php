                        <a class="question-text" href="#"  id="hideshow20" title="toggle(show/hide)"><h5>Requesting a ride with Work Me Now</h5></a>
                        <div id="message20" style=" display:none;"><p class="blurb">A Work Me Now trip begins at a pickup location and ends at your final destination. To get started, open your app.</p>
                        <p class="blurb">CHOOSE A VEHICLE OPTION</p>
                        <p class="blurb">Use the slider at the bottom of your screen to select a vehicle option. Tap any vehicle option button to see a fare estimate.</p>
                        <p class="blurb">OR CHOOSE A PAST DRIVER</p>
                        <p class="blurb">If you have used Work Me Now driver before and want to request them you will have the option to do so. This option will be accessible from the requesting a ride home screen.</p> 
                        <p class="blurb">SET YOUR PICKUP LOCATION</p>
                        <p class="blurb">Enter your pickup location using one of these methods:
                        <ul><li>move the map to drop your pin on a desired location</li>
                        <li>type the exact address or select the address from search results</li>
                        </ul></p>
                        <p class="blurb">Next, tap the SET PICKUP LOCATION bar. This bar also gives an estimated time of arrival for a driver who accepts your ride request.</p>
                        <p class="blurb">ENTER YOUR DESTINATION </p>
                        <p class="blurb">Tap Enter destination to choose where you would like to go. Similar to entering your pickup location, street addresses and location names can be used.
                        Enter your destination anytime before or after requesting a ride. You can also update your destination anytime.</p> 
                        <p class="blurb">REQUEST PICKUP</p>
                        <p class="blurb">Tap REQUEST at the bottom of your screen.
                                     Wait for a driver to accept your request. When this happens, your map will display the    
                                    driver’s location. The pin at your pickup location will show the driver's ETA. 
                        Your app notifies you about one minute before the driver arrives. Make sure you’re at the exact pickup location. 
                        To help you identify your driver, your app displays your driver’s name, vehicle make and model, and license plate number. Tap this info to access options to contact your driver by text or phone call.</p>
                        <p class="blurb">PICKUP</p>
                        <p class="blurb">When your driver arrives, it's good practice to confirm each other's names and discuss the route you will take. Check that you've entered your preferred destination in your app.</p> 
