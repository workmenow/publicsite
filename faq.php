                        <p class="blurb">When you’re heading to your destination, swipe up on the app screen to perform the following:
                        <ul><li>Split fare: share the cost of a Work Me Now trip with multiple people.</li>
                        <li>Send ETA: share your trip status with someone so they know when you should be arriving at your destination.</li>
                        <li>Change trip destination: change your destination address after you requested a ride or while you are on your way </li>
                        <li>Cancel trip: if you need to cancel your trip early, notify your driver and make sure you’re in a safe location before leaving the vehicle</li>
                        </ul>
                        </p>
                        <p class="blurb">TIP YOUR DRIVER IF YOU WOULD LIKE</p>
                        <p class="blurb">You will have the option to tip your driver with the ease of a preset dollar amount or you can enter a specific dollar amount. Remember this is optional.</p>
                        <p class="blurb">RATE YOUR DRIVER</p>
                        <p class="blurb">After a trip ends, your app will ask you to rate your driver. You can do this anytime before requesting your next ride.</p>
                        </p></div>
